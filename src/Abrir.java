            BufferedReader archivo = new BufferedReader (new FileReader (jTextField1.getText()));

            String buffer = "";
            String linea = "";

            do{
                linea=archivo.readLine();

                if(linea != null){
                    buffer += linea+"\n";
                }

            }while(linea != null);

